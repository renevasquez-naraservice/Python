# Enunciado => Pide al usuario su nombre, edad y estatura. Luego imprime un mensaje personalizado (convierte a int la edad y usa float para estatura).
# Tenemos
nombre = input("Hola Usuario. Cuál es tu nombre?: ")
edad = int(input("¿Cuántos años tienes?: "))
estatura = float(input("Eres alto? Cuanto: "))
print(f"Usted es {nombre}, tienes {edad} y una estatura de {estatura} mts. :)")